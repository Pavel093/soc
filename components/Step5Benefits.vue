  if (currentBenefits.value.regionalOther) list.push('–î—Ä—É–≥–∏–µ —Ä–µ–≥–∏–æ–Ω–∞–ª—å–Ω—ã–µ –≤—ã–ø–ª–∞—Ç—ã')
